ergebnisse = read.csv2("bin\\x64\\Release\\results.csv", colClasses = c(rep('integer', 2),rep('numeric',8)), sep=';',dec='.',row.names = NULL)
colnames(ergebnisse) = c("Size", "Iteration", "InitTran", "InitBatch", "InitInc", "MainTran", "MainBatch", "MainInc", "InitTGG", "MainTGG")
averages = aggregate(ergebnisse, list(ergebnisse$Size), mean)

size = averages$Size

pdf(file="init.pdf", width=8, height=4)
par(mar=c(4.3,4.0,0.3,0.3))
plot(size, averages$InitTGG, type="n", xlab="Number of states", ylab="Initial transformation [ms]", ylim=c(0.4,max(averages$InitTGG)), log="xy")
lines(size, averages$InitInc, col="blue")
points(size, averages$InitInc, pch=16, col="blue")
lines(size, averages$InitTGG, col="red")
points(size, averages$InitTGG, pch=2, col="red")
lines(size, averages$InitBatch, col="green")
points(size, averages$InitBatch, pch=8, col="green")
lines(size, averages$InitTran, col="orange")
points(size, averages$InitTran, pch=13, col="orange")
legend(10, max(averages$InitTGG), c("NMF Synchronizations (Incremental)", "NMF Synchronizations (Batch)", "NTL (Batch)", "eMoflon (Incremental)"), col=c('blue', 'green', 'orange', 'red'), pch=c(16, 8, 13, 2), bty='n', lty=1)
dev.off()

pdf(file="updates.pdf", width=8, height=4)
par(mar=c(4.3,4.0,0.3,0.3))
plot(size, averages$MainTGG, type="n", xlab="Number of states", ylab="Runtime for 20 changes [ms]", ylim=c(min(averages$MainInc), max(averages$MainTGG)), log="xy")
lines(size, averages$MainInc, col="blue")
points(size, averages$MainInc, pch=16, col="blue")
lines(size, averages$MainTGG, col="red")
points(size, averages$MainTGG, pch=2, col="red")
lines(size, averages$MainBatch, col="green")
points(size, averages$MainBatch, pch=8, col="green")
lines(size, averages$MainTran, col="orange")
points(size, averages$MainTran, pch=13, col="orange")
legend(10, max(averages$MainTGG), c("NMF Synchronizations (Incremental)", "NMF Synchronizations (Batch)", "NTL (Batch)", "eMoflon (Incremental)"), col=c('blue', 'green', 'orange', 'red'), pch=c(16, 8, 13, 2), bty='n', lty=1)
dev.off()